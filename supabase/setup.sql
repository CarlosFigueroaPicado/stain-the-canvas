-- Stain the Canvas - Supabase setup
-- Run in Supabase SQL Editor.
-- Security model:
-- - Public read for catalog browsing.
-- - Admin write for admin actions (app_metadata.role='admin' or app_metadata.is_admin=true).
-- - Public insert for analytics events (visitas/eventos).

create extension if not exists pgcrypto;

create table if not exists public.productos (
  id uuid primary key default gen_random_uuid(),
  nombre text not null,
  categoria text not null,
  descripcion text not null,
  precio numeric(10,2) not null default 0 check (precio >= 0),
  imagen_url text,
  gallery_urls jsonb not null default '[]'::jsonb,
  vistas integer not null default 0,
  clicks integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.productos
  add column if not exists gallery_urls jsonb not null default '[]'::jsonb,
  add column if not exists created_at timestamptz not null default now(),
  add column if not exists updated_at timestamptz not null default now(),
  add column if not exists vistas integer not null default 0,
  add column if not exists clicks integer not null default 0;

update public.productos
set gallery_urls =
  case
    when coalesce(trim(imagen_url), '') <> '' then jsonb_build_array(imagen_url)
    else '[]'::jsonb
  end
where gallery_urls is null
   or jsonb_typeof(gallery_urls) <> 'array'
   or gallery_urls = '[]'::jsonb;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_productos_updated_at on public.productos;
create trigger trg_productos_updated_at
before update on public.productos
for each row execute function public.set_updated_at();

create table if not exists public.visitas (
  id uuid primary key default gen_random_uuid(),
  fecha timestamptz not null default now(),
  session_id text not null,
  user_agent text
);

create table if not exists public.eventos (
  id uuid primary key default gen_random_uuid(),
  tipo text not null,
  producto_id uuid references public.productos(id) on delete set null,
  categoria text,
  session_id text not null,
  fecha timestamptz not null default now()
);

create table if not exists public.admin_users (
  id bigint generated by default as identity primary key,
  user_id uuid not null unique,
  email text,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create index if not exists idx_visitas_fecha on public.visitas (fecha desc);
create index if not exists idx_visitas_session_id on public.visitas (session_id);
create index if not exists idx_eventos_tipo_fecha on public.eventos (tipo, fecha desc);
create index if not exists idx_eventos_producto_id on public.eventos (producto_id);
create index if not exists idx_eventos_categoria on public.eventos (categoria);

alter table public.productos enable row level security;
alter table public.visitas enable row level security;
alter table public.eventos enable row level security;
alter table public.admin_users enable row level security;

create or replace function public.is_admin_user()
returns boolean
language sql
stable
as $$
  select
    lower(coalesce(auth.jwt() -> 'app_metadata' ->> 'role', '')) = 'admin'
    or lower(coalesce(auth.jwt() -> 'app_metadata' ->> 'is_admin', 'false')) = 'true'
    or exists (
      select 1
      from public.admin_users au
      where au.user_id = auth.uid()
        and au.is_active = true
    );
$$;

drop policy if exists "Admins can read own membership" on public.admin_users;
create policy "Admins can read own membership"
on public.admin_users
for select
using (auth.uid() = user_id);

