create or replace function public.guard_visitas_insert()
returns trigger
language plpgsql
as $$
begin
  if exists (
    select 1
    from public.visitas v
    where v.session_id = new.session_id
      and v.fecha > now() - interval '15 seconds'
  ) then
    return null;
  end if;

  if (
    select count(*)
    from public.visitas v
    where v.session_id = new.session_id
      and v.fecha > now() - interval '1 minute'
  ) >= 30 then
    return null;
  end if;

  return new;
end;
$$;

drop trigger if exists trg_guard_visitas_insert on public.visitas;
create trigger trg_guard_visitas_insert
before insert on public.visitas
for each row execute function public.guard_visitas_insert();

create or replace function public.guard_eventos_insert()
returns trigger
language plpgsql
as $$
begin
  if exists (
    select 1
    from public.eventos e
    where e.session_id = new.session_id
      and e.tipo = new.tipo
      and coalesce(e.producto_id::text, '') = coalesce(new.producto_id::text, '')
      and e.fecha > now() - interval '8 seconds'
  ) then
    return null;
  end if;

  if (
    select count(*)
    from public.eventos e
    where e.session_id = new.session_id
      and e.fecha > now() - interval '1 minute'
  ) >= 60 then
    return null;
  end if;

  return new;
end;
$$;

drop trigger if exists trg_guard_eventos_insert on public.eventos;
create trigger trg_guard_eventos_insert
before insert on public.eventos
for each row execute function public.guard_eventos_insert();

create or replace function public.get_dashboard_metrics(p_days integer default 90)
returns jsonb
language sql
stable
as $$
with params as (
  select greatest(1, least(coalesce(p_days, 90), 3650)) as days
),
cutoff as (
  select now() - make_interval(days => (select days from params)) as ts
),
visitas_src as (
  select fecha
  from public.visitas v
  where v.fecha >= (select ts from cutoff)
),
eventos_src as (
  select tipo, producto_id, categoria, fecha
  from public.eventos e
  where e.fecha >= (select ts from cutoff)
),
totales as (
  select
    (select count(*) from visitas_src) as total_visitas,
    (select count(*) from eventos_src where tipo = 'view_producto') as total_vistas,
    (select count(*) from eventos_src where tipo = 'click_whatsapp') as total_clicks
),
visitas_serie as (
  select
    to_char(date_trunc('day', fecha), 'YYYY-MM-DD') as day,
    count(*)::int as total
  from visitas_src
  group by 1
  order by 1
),
top_productos as (
  select
    es.producto_id,
    coalesce(p.nombre, concat('Producto ', left(es.producto_id::text, 8))) as nombre,
    coalesce(nullif(p.categoria, ''), nullif(es.categoria, ''), 'Sin categoria') as categoria,
    count(*)::int as vistas
  from eventos_src es
  left join public.productos p on p.id = es.producto_id
  where es.tipo = 'view_producto'
    and es.producto_id is not null
  group by es.producto_id, nombre, categoria
  order by vistas desc
  limit 10
),
categorias as (
  select
    coalesce(nullif(p.categoria, ''), nullif(es.categoria, ''), 'Sin categoria') as categoria,
    count(*)::int as total
  from eventos_src es
  left join public.productos p on p.id = es.producto_id
  where es.tipo = 'view_producto'
  group by 1
  order by total desc
)
select jsonb_build_object(
  'totals', jsonb_build_object(
    'totalVisitas', coalesce((select total_visitas from totales), 0),
    'totalVistas', coalesce((select total_vistas from totales), 0),
    'totalClicks', coalesce((select total_clicks from totales), 0),
    'conversion', case
      when coalesce((select total_vistas from totales), 0) > 0
        then round(((select total_clicks from totales)::numeric / (select total_vistas from totales)::numeric) * 100, 1)
      else 0
    end
  ),
  'visitSeries', coalesce(
    (select jsonb_agg(jsonb_build_object('day', day, 'total', total) order by day) from visitas_serie),
    '[]'::jsonb
  ),
  'topProducts', coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'id', producto_id,
          'nombre', nombre,
          'categoria', categoria,
          'vistas', vistas
        )
        order by vistas desc
      )
      from top_productos
    ),
    '[]'::jsonb
  ),
  'byCategory', coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'categoria', categoria,
          'total', total
        )
        order by total desc
      )
      from categorias
    ),
    '[]'::jsonb
  )
);
$$;
