-- Stain the Canvas - Analisis de clientes anonimo (sin PII)
-- Run in Supabase SQL Editor.
-- Este script NO guarda datos personales (nombre, correo, telefono, direccion).
-- Se centra en sesion anonima + eventos de comportamiento para medir embudo hacia WhatsApp.

create extension if not exists pgcrypto;
create schema if not exists private;

-- Catalogo de fuentes de trafico (normalizado)
create table if not exists public.fuentes_trafico (
  id smallint generated by default as identity primary key,
  codigo text not null unique,
  nombre text not null,
  created_at timestamptz not null default now()
);

-- Catalogo de tipos de evento (normalizado)
create table if not exists public.tipos_evento_cliente (
  id smallint generated by default as identity primary key,
  codigo text not null unique,
  descripcion text,
  created_at timestamptz not null default now()
);

-- Sesiones anonimas (cliente no identificado)
create table if not exists public.sesiones_cliente_anonimas (
  id uuid primary key default gen_random_uuid(),
  session_id text not null unique,
  fuente_id smallint references public.fuentes_trafico(id) on delete set null,
  landing_path text,
  user_agent text,
  first_seen_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  created_at timestamptz not null default now()
);

-- Eventos anonimos por sesion
create table if not exists public.eventos_cliente_anonimos (
  id uuid primary key default gen_random_uuid(),
  sesion_id uuid not null references public.sesiones_cliente_anonimas(id) on delete cascade,
  tipo_evento_id smallint not null references public.tipos_evento_cliente(id) on delete restrict,
  producto_id uuid references public.productos(id) on delete set null,
  categoria text,
  meta jsonb not null default '{}'::jsonb,
  fecha timestamptz not null default now()
);

create index if not exists idx_fuentes_trafico_codigo on public.fuentes_trafico (codigo);
create index if not exists idx_tipos_evento_cliente_codigo on public.tipos_evento_cliente (codigo);
create index if not exists idx_sesiones_cliente_anonimas_session_id on public.sesiones_cliente_anonimas (session_id);
create index if not exists idx_sesiones_cliente_anonimas_first_seen on public.sesiones_cliente_anonimas (first_seen_at desc);
create index if not exists idx_sesiones_cliente_anonimas_last_seen on public.sesiones_cliente_anonimas (last_seen_at desc);
create index if not exists idx_eventos_cliente_anonimos_sesion_id on public.eventos_cliente_anonimos (sesion_id);
create index if not exists idx_eventos_cliente_anonimos_tipo_fecha on public.eventos_cliente_anonimos (tipo_evento_id, fecha desc);
create index if not exists idx_eventos_cliente_anonimos_producto_id on public.eventos_cliente_anonimos (producto_id);
create index if not exists idx_eventos_cliente_anonimos_categoria on public.eventos_cliente_anonimos (categoria);

